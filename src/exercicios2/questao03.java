/*
Crie um programa em java que imprima uma sequência de números de 0 a 100
usando estruturas de repetição.
*/

package exercicios2;
public class questao03{
    public static void main(String[] args){
        
        int num = 0;
        
        while(num <= 100){
            
            System.out.println(num);
            num++;
        }
    }
}